package com.ThinkingInJava;

public class DataOnly {
    int i= 56;
    double d = 21.21;
    boolean b = true;

    public static void main(String[] args) {
        DataOnly dataOnly = new DataOnly();
        System.out.println(dataOnly.i);
        System.out.println(dataOnly.d);
        System.out.println(dataOnly.b);
    }
}
